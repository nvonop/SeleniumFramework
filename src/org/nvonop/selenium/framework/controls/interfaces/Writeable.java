package org.nvonop.selenium.framework.controls.interfaces;

/**
 * This interface provides functionality for writing data into a control.
 * 
 * @author nvonop
 * 
 */
public interface Writeable {

	/**
	 * This method will write the supplied data into the control.
	 * 
	 * @param value
	 */
	void write(String value);
}
